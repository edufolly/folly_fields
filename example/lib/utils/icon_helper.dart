    Set<IconData> seen = <IconData>{};
    Map<String, IconData> unq = <String, IconData>{};

    for (final MapEntry<String, IconData> entry in data.entries) {
      if (!seen.contains(entry.value)) {
        unq[entry.key] = entry.value;
        seen.add(entry.value);
      }
    }

    return unq;
